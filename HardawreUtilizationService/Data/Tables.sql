CREATE TABLE UtilizationValues (
	"Id"	INTEGER NOT NULL UNIQUE,
	"CPU"	TEXT NOT NULL,
	"DISK"	TEXT NOT NULL,
	"MEMORY"	TEXT NOT NULL,
	PRIMARY KEY("Id" AUTOINCREMENT)
);

CREATE TABLE HardwareTypes(
Id INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
Model TEXT NOT NULL,
AdditionalInfo TEXT NOT NULL);

CREATE TABLE Records(
Id INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
HardwareType INTEGER NOT NULL,
Value INTEGER NOT NULL,
CreateDate DATETIME,
CONSTRAINT fk_hardwaretype 
FOREIGN KEY (HardwareType)
REFERENCES HardwareTypes(Id),
CONSTRAINT fk_value
FOREIGN KEY (Value)
REFERENCES UtilizationValues(Id)
);